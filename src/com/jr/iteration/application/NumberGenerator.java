package com.jr.iteration.application;

class NumberGenerator {
		
	public static void printTenTimes(int noOfLoops) {
		// Create a method top print out the numbers 1 to 10 as many times as the value of that number
		 // create a method that can print out the numbers 1-10 10 times each
		
		
		int j=0;
		
		while (j<noOfLoops) {
			for(int i=1; i<=10; i++) {
				System.out.println(i);	
			}
			j++;
		
	}
}
		

}
